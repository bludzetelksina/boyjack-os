; kernel/boot/multiboot_header.asm
; Multiboot заголовок для совместимости с загрузчиками (например, GRUB)

section .multiboot
align 4

multiboot_header:
    dd 0x1BADB002          ; magic number Multiboot
    dd 0x00010003          ; флаги: 0x00010003 - требовать память, загрузка в защищенный режим, и выравнивание
    dd -(0x1BADB002 + 0x00010003) ; checksum (так, чтобы сумма всех трех чисел = 0)

; Далее можно добавить поля для адреса загрузчика, кода ядра и т.д.
; Но минимальный заголовок состоит из этих трех полей.

; Обязательный код загрузчика начинается сразу после этого заголовка
; (все должно быть в одном сегменте/файле)

; Минимальная загрузочная сигнатура для BIOS (если нужно)
times 512 - ($ - $$) db 0
dw 0xAA55
