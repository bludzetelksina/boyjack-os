#![no_std]

pub mod memory;
pub mod scheduler;
pub mod devices;

pub fn init_system() {
    // Инициализация менеджера памяти (страничная таблица, аллокатор)
    memory::init();

    // Инициализация драйверов устройств (например, видео, клавиатура)
    devices::init();

    // Инициализация планировщика задач и запуска первой задачи
    scheduler::init();

    // Можно добавить инициализацию других подсистем (файловая система, сеть и т.п.)
}
